    private void DeleteStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteStudentButtonActionPerformed
        // TODO add your handling code here:
        int row = studentTable.getSelectedRow();
        
        if(row == -1)
        {
            JOptionPane.showMessageDialog(MainFrame.this, "Vui Long Chon Student Truoc","Loi",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            int submit = JOptionPane.showConfirmDialog(MainFrame.this, "Ban Co Chac Chan Muon Xoa Khong?");
            
            if(submit == JOptionPane.YES_OPTION)
            {
                int studentId = Integer.parseInt(String.valueOf(studentTable.getValueAt(row, 0)));
                defauTableModel.setRowCount(0);
                studentmanagement.deleteStudentById(studentId);
                setTableData((studentmanagement.getAllStudent()));
                studentmanagement.saveToFile();
            }
        }
    }//GEN-LAST:event_DeleteStudentButtonActionPerformed

    private void OkFindButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OkFindButtonActionPerformed
        // TODO add your handling code here:
        defauTableModel.setRowCount(0);      
        String x =(String) StudentSpinner.getValue();
        if(x.equals("Sid"))
        {
            int sid = Integer.parseInt(FindIFText.getText());
            Student Sid = studentmanagement.findStudentById(sid);
            System.out.println(studentmanagement.findStudentById(sid).toString());
            
            ArrayList<Student> StudentById = new ArrayList();
            StudentById.add(Sid);
            setTableData(StudentById);
            
        }
        
        if(x.equals("Major"))
        { 
            String major = FindIFText.getText();
            ArrayList<Student> StudentByMajor = studentmanagement.findStudentByMajor(major);
            System.out.println(StudentByMajor.toString());
            
            setTableData(StudentByMajor);
        }
       
    }//GEN-LAST:event_OkFindButtonActionPerformed

    private void SortButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SortButtonActionPerformed
        // TODO add your handling code here:
        
        if(SidRadioButton.isSelected())
        {
            studentmanagement.sortById();
        }
        if(GpaRadioButton.isSelected())
        {
            studentmanagement.sortByGpa();
        }
        if(FNameRadioButton.isSelected())
        {
            studentmanagement.sortByFirstName();
        }
        
        defauTableModel.setRowCount(0);      
        setTableData((studentmanagement.getAllStudent()));
    }//GEN-LAST:event_SortButtonActionPerformed
    
    private void setTableData(ArrayList<Student> students)
    {
        for(Student student: students)
        {
            defauTableModel.addRow(new Object[]{student.getSid(), student.getFirstName(), 
                student.getLastName(), student.getMajor(), student.getGpa()});
        }
    }
