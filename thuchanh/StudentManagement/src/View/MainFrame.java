    private void UpdateStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateStudentButtonActionPerformed
        
        int row = studentTable.getSelectedRow();
        
        if(row == -1)
        {
            JOptionPane.showMessageDialog(MainFrame.this, "Vui Long Chon Student Truoc","Loi",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
                int sid = Integer.parseInt(String.valueOf(studentTable.getValueAt(row, 0)));
                
                new UpdateFrame(sid).setVisible(true);
                dispose();
            
        }
    }//GEN-LAST:event_UpdateStudentButtonActionPerformed

